#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*tmp;

	if (!f || !lst)
		return ;
	while (tmp)
	{
		tmp = lst->next;
		f(lst->content);
		lst = tmp;
	}
}
